public class myCalculatorImpl implements MyCalculator{

    @Override
    public double getSum(int num1, int num2) {
        return num1+num2;
    }

    @Override
    public double getMultiplication(int num1, int num2) {
        return num1+num2;
    }

    @Override
    public double getSubtraction(int num1, int num2) {
        return num1-num2;
    }
}
